    public void checkValues()
    {
        
        Thread t = new Thread()
        {
          public void run()
          {
              while(true)
                {
                if( burner != null )
                {
                    elementStatusLabel.setText(burner.isOn()?"ON":"OFF");
                }
                
                  try
                  {
                      Thread.sleep(50);
                  }
                  catch (InterruptedException ex)
                  {
                      Logger.getLogger(Simulator.class.getName()).log(Level.SEVERE, null, ex);
                  }
                }
          }
        };
        
        t.start();
       
    }

