    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jToggleButton1ActionPerformed
    {//GEN-HEADEREND:event_jToggleButton1ActionPerformed
        Double gallons = Double.parseDouble(kettleGallons.getText());
        Double watts = Double.parseDouble(kettleWatts.getText());
        Double temp = 5.0d/9.0d*(Double.parseDouble(kettleTemp.getText())-32.0d);
        
        burner = new Burner(gallons, watts, temp, kettleElementGPIO.getText(), kettleProbeName.getText());
        burner.start();
        checkValues();
    }//GEN-LAST:event_jToggleButton1ActionPerformed

